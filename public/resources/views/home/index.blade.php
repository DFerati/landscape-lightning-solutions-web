        function handleIntersectionFade(entries, observer) {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('opacity-100');
                    observer.unobserve(entry.target);
                }
            });
        }

        function createObserverFade(targetElementId) {
            const options = {
                root: null,
                rootMargin: '0px',
                threshold: 0.3
            };

            const observer = new IntersectionObserver(handleIntersectionFade, options);
            const targetElement = document.getElementById(targetElementId);

            if (targetElement) {
                observer.observe(targetElement);
            }
        }

        createObserverFade('fadeIn-element-1');
